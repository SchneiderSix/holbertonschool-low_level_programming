#include "3-calc.h"

/**
 * op_add - sum
 * @a: var
 * @b: var
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *  * op_sub - difference
 *   * @a: var
 *    * @b: var
 *     * Return: result
 *      */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *  * op_mul - multiplication
 *   * @a: var
 *    * @b: var
 *     * Return: result
 *      */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *  * op_div - div
 *   * @a: var
 *    * @b: var
 *     * Return: result
 *      */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *  * op_mod - resto de division
 *   * @a: var
 *    * @b: var
 *     * Return: result
 *      */
int op_mod(int a, int b)
{
	return (a % b);
}
